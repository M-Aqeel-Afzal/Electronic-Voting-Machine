package application;

public class Party {
private String Name;
private String Chairman;
private String cnic;
public String getName() {
	return Name;
}
public void setName(String name) {
	Name = name;
}
public String getChairman() {
	return Chairman;
}
public void setChairman(String chairman) {
	Chairman = chairman;
}
public String getCnic() {
	return cnic;
}
public void setCnic(String cnic) {
	this.cnic = cnic;
}

}
