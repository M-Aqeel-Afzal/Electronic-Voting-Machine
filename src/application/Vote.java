package application;

public class Vote {
private String cinc;
private String partyn;
private String candidaten;
public String getCinc() {
	return cinc;
}
public void setCinc(String cinc) {
	this.cinc = cinc;
}
public String getPartyn() {
	return partyn;
}
public void setPartyn(String partyn) {
	this.partyn = partyn;
}
public String getCandidaten() {
	return candidaten;
}
public void setCandidaten(String candidaten) {
	this.candidaten = candidaten;
}
}
